import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { projectName, description, targetUsers, goals, features } = await req.json();
    
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are a senior product manager creating comprehensive Product Requirements Documents (PRDs). 
Generate a well-structured PRD in Markdown format with the following sections:

1. **Executive Summary** - Brief overview of the product
2. **Problem Statement** - What problem does this solve?
3. **Goals & Success Metrics** - Measurable objectives
4. **Target Users** - User personas and their needs
5. **User Stories** - Key user scenarios in "As a... I want... So that..." format
6. **Functional Requirements** - Detailed feature specifications
7. **Non-Functional Requirements** - Performance, security, scalability
8. **Technical Considerations** - Architecture suggestions
9. **Timeline & Milestones** - Suggested phases
10. **Risks & Mitigations** - Potential challenges
11. **Open Questions** - Items needing clarification

Be specific, actionable, and professional. Use bullet points and clear formatting.`;

    const userPrompt = `Create a PRD for the following product:

**Product Name:** ${projectName}

**Description:** ${description}

**Target Users:** ${targetUsers}

**Goals:** ${goals}

**Key Features:** ${features}

Generate a comprehensive PRD that a development team can use to build this product.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: userPrompt },
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Payment required. Please add credits." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
